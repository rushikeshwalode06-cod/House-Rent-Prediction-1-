import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('House_Rent.txt')

data

data.describe(include='all')

data.isnull()

data.isnull().sum()

data

data['Furnishing'] = data['Furnishing'].map({'Unfurnished':0,'Semi-Furnished':1,'Furnished':2})

data

data.Location.unique

data = data.drop('Location',axis=1)

data

X = data.drop('Rent',axis=1)

X

y   = data.Rent

y

from sklearn.model_selection import train_test_split

X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=0)

X_train.shape

X_test.shape

from sklearn.linear_model import LinearRegression

model = LinearRegression()

model.fit(X_train,y_train)

y_pred = model.predict(X_test)

y_pred

from sklearn.metrics import mean_absolute_error

print("Error:", mean_absolute_error(y_test, y_pred))

def predict_rent(bhk, size, bathroom, furnishing):
    return model.predict([[bhk, size, bathroom, furnishing]])

predict_rent(2,100,2,3)

predict_rent(3,1000,1,1)

predict_rent(2,2000,2,2)





